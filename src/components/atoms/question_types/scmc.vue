<template>
  <!-- single correct multiple choice -->
  <!-- responses can be integer 0 to n, i.e. the index of the chosen option -->
  <div class="flex flex-col">
    <div
      v-for="(option, optIndex) in question.options"
      :key="optIndex"
      class="cursor-pointer flex flex-row hover:bg-blue-100"
      @click="inputVal = optIndex"
    >
      <FontAwesomeIcon
        :icon="optIndex === inputVal ? ['fas', 'circle'] : ['far', 'circle']"
        class="mr-2 mt-2"
      />
      <div class="w-full">
        <LazyTextInputForEditor
          v-if="getEditMode"
          v-model="option.title"
          :placeholder="'option ' + String(optIndex + 1)"
          class="flex-grow"
        />
        <div
          v-else-if="option.title"
          class="prose"
          v-html="$md.render(parseText(option.title))"
        />
      </div>
      <!-- <img
        v-if="option.foto"
        :src="option.foto"
        class="h-48 pl-10 rounded-md"
      /> -->
      <!-- <FontAwesomeIcon
            v-if="getEditMode"
            class="text-gray-500 hover:text-black cursor-pointer flex-none mx-1"
            :icon="['far', 'image']"
          /> -->
      <FontAwesomeIcon
        v-if="getEditMode"
        :icon="['fas', 'times']"
        class="text-gray-500 hover:text-black cursor-pointer mx-1 mt-2"
        @click="
          DELETE_QUESTION_OPTION({
            sectionIndex: getSectionIndex,
            qnIndex: index,
            optIndex,
          })
        "
      />
    </div>

    <div
      v-if="getEditMode"
      class="mt-1 cursor-pointer"
      @click="ADD_QUESTION_OPTION({ index, sectionIndex: getSectionIndex })"
    >
      <FontAwesomeIcon :icon="['fas', 'plus']" />
      <p class="inline">add option</p>
    </div>

    <div
      v-if="inputVal !== null"
      class="font-normal text-lg text-teal-800 mt-5 cursor-pointer"
      @click="inputVal = null"
    >
      Clear Selection
    </div>
  </div>
</template>

<script>
import { mapGetters, mapMutations } from 'vuex'

export default {
  props: {
    index: Number(),
  },
  computed: {
    ...mapGetters([
      'parseText',
      'getSectionIndex',
      'getEditMode',
      'getQuestion',
      'getQuestionResponse',
    ]),
    question() {
      return this.getQuestion(this.index)
    },
    response() {
      return this.getQuestionResponse(this.index)
    },
    inputVal: {
      get() {
        return this.response ? Number(this.response) : null
      },
      set(value) {
        if (this.getEditMode) return
        this.SET_RESPONSE({
          qnIndex: this.index,
          newVal: value !== null ? String(value) || '0' : null,
        })
      },
    },
  },
  methods: {
    ...mapMutations([
      'ADD_QUESTION_OPTION',
      'SET_RESPONSE',
      'DELETE_QUESTION_OPTION',
    ]),
  },
}
</script>
